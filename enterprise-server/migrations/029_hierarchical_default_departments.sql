-- Add hierarchical departments and seed the default Linewell organization tree.
--
-- Department codes come from the source organization system and are unique
-- within an organization. parent_id is nullable so existing flat departments
-- remain valid and NULL continues to represent a root department.

ALTER TABLE departments
    ADD COLUMN IF NOT EXISTS code TEXT,
    ADD COLUMN IF NOT EXISTS parent_id UUID;

-- Reuse the original default technology department so existing memberships
-- keep pointing at the imported 技术中心 node.
UPDATE departments
SET code = 'F01289'
WHERE org_id = (SELECT id FROM organizations WHERE slug = 'linewell.com')
  AND slug = 'technology-center'
  AND code IS NULL;

-- The old generic 研发中心 seed has no exact node in the source tree. Remove it
-- only when it is unused; existing installations keep referenced legacy data.
DELETE FROM departments d
WHERE d.org_id = (SELECT id FROM organizations WHERE slug = 'linewell.com')
  AND d.slug = 'rd-center'
  AND d.code IS NULL
  AND NOT EXISTS (
      SELECT 1
      FROM org_members om
      WHERE om.department_id = d.id
  );

UPDATE departments
SET code = 'LEGACY-' || upper(replace(id::text, '-', ''))
WHERE code IS NULL;

ALTER TABLE departments
    ALTER COLUMN code SET DEFAULT ('D-' || upper(replace(gen_random_uuid()::text, '-', ''))),
    ALTER COLUMN code SET NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS idx_departments_org_code
    ON departments (org_id, code);


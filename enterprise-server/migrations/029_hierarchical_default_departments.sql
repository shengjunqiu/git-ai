INSERT INTO departments (id, org_id, code, name, slug)
SELECT
    seed.id,
    organization.id,
    seed.code,
    seed.name,
    seed.slug
FROM default_department_seed seed
JOIN organizations organization ON organization.slug = 'linewell.com'
ON CONFLICT (org_id, code) DO UPDATE
SET name = EXCLUDED.name;

UPDATE departments child
SET parent_id = parent.id
FROM default_department_seed seed
JOIN organizations organization ON organization.slug = 'linewell.com'
LEFT JOIN departments parent
    ON parent.org_id = organization.id
   AND parent.code = seed.parent_code
WHERE child.org_id = organization.id
  AND child.code = seed.code
  AND child.parent_id IS DISTINCT FROM parent.id;

CREATE UNIQUE INDEX IF NOT EXISTS idx_departments_org_id_id
    ON departments (org_id, id);

CREATE INDEX IF NOT EXISTS idx_departments_org_parent
    ON departments (org_id, parent_id);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'departments_parent_same_org_fkey'
    ) THEN
        ALTER TABLE departments
            ADD CONSTRAINT departments_parent_same_org_fkey
            FOREIGN KEY (org_id, parent_id)
            REFERENCES departments (org_id, id)
            ON DELETE RESTRICT;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'departments_parent_not_self'
    ) THEN
        ALTER TABLE departments
            ADD CONSTRAINT departments_parent_not_self
            CHECK (parent_id IS NULL OR parent_id <> id);
    END IF;
END
$$;

